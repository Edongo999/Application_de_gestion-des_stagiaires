        public void tableEtablissement() {
        String[] Champ = {"Nom Etablissement","Ville","Pays","ID"};
        String[] afficher = new String[4];
        DefaultTableModel model = new DefaultTableModel(null, Champ);
        try {
            connect con = new connect();
            pst = con.con.prepareStatement("select * from etablissement");
            rs = pst.executeQuery();
            int i = 0;
            while (rs.next()) {
                afficher[0] = rs.getString("nometab");
                afficher[1] = rs.getString("ville");
                afficher[2] = rs.getString("pays");
                afficher[3] = rs.getString("idetab");
                i++;
                model.addRow(afficher);
            }
            txtResultat_Trouver.setText(i + " : Resultat trouver");
            tableEtablissement.setModel(model);
        } catch (Exception e) {
        }

    }

 

    public void rechercheEtablissement() {
        String[] Champ = {"Nom Etablissement","Ville","Pays","ID"};
        String[] afficher = new String[4];
        DefaultTableModel model = new DefaultTableModel(null, Champ);
        try {
            connect con = new connect();
            pst = con.con.prepareStatement("select * from etablissement where nometab like '%" + txtrechercheEtablissement.getText().trim() + "%' ");
            rs = pst.executeQuery();
            int i = 0;
            while (rs.next()) {
                afficher[0] = rs.getString("nometab");
                afficher[1] = rs.getString("Ville");
                afficher[2] = rs.getString("Pays");
                afficher[3] = rs.getString("idetab");
                i++;
                txtnometab.setText(afficher[0]);
                txtville.setText(afficher[1]);
                txtpays.setText(afficher[2]);
                txtidetab.setText(afficher[3]);
             

                model.addRow(afficher);
            }
            txtResultat_Trouver.setText(i + ":Resultat trouver");
           tableEtablissement.setModel(model);
        } catch (Exception e) {
        }

    }
    
    
