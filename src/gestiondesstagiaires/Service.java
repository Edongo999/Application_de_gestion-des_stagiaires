        public void tableService() {
        String[] Champ = {"Nom Service","Direction","ID"};
        String[] afficher = new String[3];
        DefaultTableModel model = new DefaultTableModel(null, Champ);
        try {
            connect con = new connect();
            pst = con.con.prepareStatement("select * from service");
            rs = pst.executeQuery();
            int i = 0;
            while (rs.next()) {
                afficher[0] = rs.getString("nomservice");
                afficher[1] = rs.getString("lieu");
                afficher[2] = rs.getString("idservice");
                i++;
                model.addRow(afficher);
            }
            txtResultat_Trouver.setText(i + " : Resultat trouver");
            tableService.setModel(model);
        } catch (Exception e) {
        }

    }

 

    public void rechercheService() {
        String[] Champ = {"Nom Service","Direction","ID"};
        String[] afficher = new String[3];
        DefaultTableModel model = new DefaultTableModel(null, Champ);
        try {
            connect con = new connect();
            pst = con.con.prepareStatement("select * from service where nomservice like '%" + txtrechercheService.getText().trim() + "%' ");
            rs = pst.executeQuery();
            int i = 0;
            while (rs.next()) {
                afficher[0] = rs.getString("nomservice");
                afficher[1] = rs.getString("lieu");
                afficher[2] = rs.getString("idservice");
                i++;
                txtnomservice.setText(afficher[0]);
                txtlieu.setText(afficher[1]);
                txtidservice.setText(afficher[2]);
             

                model.addRow(afficher);
            }
            txtResultat_Trouver.setText(i + ":Resultat trouver");
           tableService.setModel(model);
        } catch (Exception e) {
        }

    }
    
    
